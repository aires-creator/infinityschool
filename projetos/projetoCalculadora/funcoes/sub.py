def dois   (num1: float, num2: float)                              -> str:
    return f"resultado = {num1 - num2}"
def tres   (num1: float, num2: float, num3: float)                 -> str:
    return f"resultado = {num1 - num2 - num3}"
def quatro (num1: float, num2: float, num3: float, num4: float)    -> str:
    return f"resultado = {num1 - num2 - num3 - num4}"
#----------|                  receives float                  | returns string |